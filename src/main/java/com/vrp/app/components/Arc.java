public class Arc {
    private int n1;
    private int n2;

    public Arc(int a, int b) {
        this.n1 = a;
        this.n2 = b;
    }

    public int getN1() {
        return n1;
    }

    public void setN1(int n1) {
        this.n1 = n1;
    }

    public int getN2() {
        return n2;
    }

    public void setN2(int n2) {
        this.n2 = n2;
    }
}
